/**
 * Return the Chinese company name for a Taiwan ticker.
 * Returns null if not found in the static table.
 */
export function getStockName(ticker: string): string | null {
  return STOCK_NAMES[ticker.toUpperCase()] ?? null;
}

/**
 * Format ticker with name: "2330 台積電"
 * Falls back to just the ticker if name not found.
 */
export function formatTicker(ticker: string, nameOverride?: string): string {
  const name = nameOverride ?? getStockName(ticker);
  return name ? `${ticker} ${name}` : ticker;
}
